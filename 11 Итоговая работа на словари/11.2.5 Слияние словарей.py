# –ø–µ—Ä–µ–¥–∞–Ω–Ω–æ–≥–æ —Å–ø–∏—Å–∫–∞.

def merge(values):      # values - —ç—Ç–æ —Å–ø–∏—Å–æ–∫ —Å–ª–æ–≤–∞—Ä–µ–π
    res = {}
    for d in values:
        for k, v in d.items():
            res[k] = res.get(k, set())
            res[k].add(v)
    return res
