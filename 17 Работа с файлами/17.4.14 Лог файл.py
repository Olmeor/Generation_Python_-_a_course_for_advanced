def minutes(x):
    res = [int(i) for i in x.split(':')]
    return res[0] * 60 + res[1]

with open('logfile.txt', 'r', encoding='utf-8') as log, open('output.txt', 'w', encoding='utf-8') as out:
    for line in log:
        user = line.split(', ')
        if minutes(user[2]) - minutes(user[1]) >= 60:
            print(user[0], file=out)
