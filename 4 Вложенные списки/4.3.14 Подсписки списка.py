def chunked(s):
    s = s.split()
    arr = [[]]
    for i in range(1, len(s) + 1):
        k = 0
        for j in range(i, len(s) + 1):
            arr.append(s[k:j])
            k += 1
    return arr


text = input()
print(chunked(text))
